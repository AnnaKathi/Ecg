//---------------------------------------------------------------------------
#pragma hdrstop

#include "classEcg.h"
//---------------------------------------------------------------------------
#pragma package(smart_init)
//---------------------------------------------------------------------------
cEcg::cEcg()
	: frpeaks(new cRpeaks), fqrs(new cQrs), fheart(new cHeartbeats), fdata(new cData)
	{
	fNumber  = -1;
	fEcgFile = "";
	fImage   = NULL;
	}
//---------------------------------------------------------------------------
cEcg::~cEcg()
	{
	if (frpeaks) delete frpeaks;
	if (fqrs)    delete fqrs;
	if (fheart)  delete fheart;
	if (fdata)   delete fdata;
	}
//---------------------------------------------------------------------------
/***************************************************************************/
/**************   getter und setter   **************************************/
/***************************************************************************/
//---------------------------------------------------------------------------
cData& cEcg::get_data()
	{
	return *fdata;
	}
//---------------------------------------------------------------------------
cRpeaks& cEcg::get_rpeaks()
	{
	return *frpeaks;
	}
//---------------------------------------------------------------------------
cQrs& cEcg::get_qrs()
	{
	return *fqrs;
	}
//---------------------------------------------------------------------------
cHeartbeats& cEcg::get_heart()
	{
	return *fheart;
	}
//---------------------------------------------------------------------------
//----  getter und setter der Hilfsvariable  --------------------------------
//---------------------------------------------------------------------------
bool cEcg::set_number(int nr)
	{
	if (nr < 0) return false;
	fNumber = nr;
    return true;
	}
//---------------------------------------------------------------------------
int cEcg::get_number()
	{
	return fNumber;
	}
//---------------------------------------------------------------------------
bool cEcg::set_file(String file)
	{
	if (file == "" || !FileExists(file)) return false;
	fEcgFile = file;
	return true;
	}
//---------------------------------------------------------------------------
String cEcg::get_file()
	{
	return fEcgFile;
	}
//---------------------------------------------------------------------------
bool cEcg::set_image(TImage* img)
	{
	fImage = img;
	return true;
	}
//---------------------------------------------------------------------------
TImage* cEcg::get_image()
	{
	return fImage;
	}
//---------------------------------------------------------------------------
